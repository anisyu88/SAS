<?php

// Include the main TCPDF library (search for installation path).
require_once('../../TCPDF-main/tcpdf.php');

// extend TCPF with custom functions
class MYPDF extends TCPDF {

    // Load table data from file
    public function LoadData() {
        // Read file lines
        include("../../db.php");
        $select = "SELECT * FROM apply WHERE status = 'approved'";

        $query = mysqli_query($con,$select);
        return $query;
    }

    // Colored table
    public function ColoredTable($header,$data) {
        // Colors, line width and bold font
        $this->SetFillColor(192, 192, 192); // Light Gray for fill
        $this->SetTextColor(0, 0, 0); // Black text for high contrast
        $this->SetDrawColor(105, 105, 105); // Dim Gray for draw color
        $this->SetLineWidth(0.3);
        $this->SetFont('', 'B');

        // Colors, line width and bold font
          //  $this->SetFillColor(100, 149, 237); // Cornflower Blue for fill
            //$this->SetTextColor(255, 255, 255); // White text
           // $this->SetDrawColor(65, 105, 225); // Royal Blue for draw color
           //$this->SetLineWidth(0.3);
           // $this->SetFont('', 'B');
        // Header
        $w = array(25, 40, 100, 30, 35, 35);
        $num_headers = count($header);
        for($i = 0; $i < $num_headers; ++$i) {
            $this->Cell($w[$i], 7, $header[$i], 1, 0, 'C', 1);
        }
        $this->Ln();
        // Color and font restoration
        $this->SetFillColor(224, 235, 255);
        $this->SetTextColor(0);
        $this->SetFont('');
        // Data
        $fill = 0;
        foreach($data as $row) {

            $this->Cell($w[0], 6, $row["applyid"], 'LR', 0, 'L', $fill);
            $this->Cell($w[1], 6, $row["studid"], 'LR', 0, 'L', $fill);
            $this->Cell($w[2], 6, $row["studname"], 'LR', 0, 'L', $fill);
            $this->Cell($w[3], 6, number_format($row["sem"]), 'LR', 0, 'C', $fill);
            $this->Cell($w[4], 6, $row["apply_date"], 'LR', 0, 'L', $fill);
            $this->Cell($w[5], 6, $row["status"], 'LR', 0, 'L', $fill);
            
            $this->Ln();
            $fill=!$fill;
        }
        $this->Cell(array_sum($w), 0, '', 'T');
    }
}

// create new PDF document
$pdf = new MYPDF(PDF_PAGE_ORIENTATION, PDF_UNIT, PDF_PAGE_FORMAT, true, 'UTF-8', false);

// set document information
$pdf->SetCreator(PDF_CREATOR);
$pdf->SetAuthor('SAS');
$pdf->SetTitle('Approved Applications');
$pdf->SetSubject('APPLY');
$pdf->SetKeywords('TCPDF, PDF, example, test, guide');

// set default header data
//$pdf->SetHeaderData(PDF_HEADER_LOGO, PDF_HEADER_LOGO_WIDTH,'Siswi Accommodation System',' Approved Applications','Accommodation Approvals');
// set default header data
$pdf->SetHeaderData(PDF_HEADER_LOGO, PDF_HEADER_LOGO_WIDTH, 'Approved Applications', 'Generated by Siswi Accommodation System');

// set header and footer fonts
